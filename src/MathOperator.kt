fun main() {

    val result = 10.0 / 3.0
    println(result)

    val result2 = 10 + 10 / 2
    println(result2)

    var total: Int = 0

    // Augmented Assignment
    val a = 100
    total += a

    val b = 200
    total += b

    val c = 300
    total += c

    println(total)

    // Unary Operator
    // total -- // total - 1
    // total -- // total - 1
    total ++ // total + 1
    total ++ // total + 1
    println(total)

    val suhu = -5
    println(suhu)

    val healthy = true
    println(!healthy)
}